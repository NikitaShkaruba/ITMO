function isValidInput() {
    if (isset($_GET['x']) && isset($_GET['y']) && isset($_GET['r'])) {
        $x = $_GET['x'];
        $y = $_GET['y'];
        $r = $_GET['r'];

        if (is_numeric($x) && is_numeric($y) && is_numeric($r))
            if ($x >= -3 & $x <= 5 && $y >= -3 && $y <= 3 && $r >= 1 && $r <= 3)
                return true;
    }

    return false;
}
function isPointInsideShape($x, $y, $r) {
    if ($x >= 0) {
        if ($y >= 0) {
            if ($x <= $r && $y <= $r)
                return true;
            else
                return false;
        }
        if ($y < 0) {
            if ($y >= -$r +$x)
                return true;
            else
                return false;
        }
    } else {
        if ($y >= 0) {
            if ($x*$x + $y*$y < $r*$r) {
                return true;
            } else
                return false;
        } 
        if ($y < 0) {
            return false;
        }
    }
}

